package com.liferay.portalweb.portlet.addapplication.contentmanagement.searchimagegallery;

import com.liferay.portalweb.portal.BaseTestCase;
import com.liferay.portalweb.portal.util.RuntimeVariables;

/**
 * @author Brian Wing Shun Chan
 */
public class SearchImageGalleryTest extends BaseTestCase {
	public void testSearchImageGallery() throws Exception {
		selenium.open("/web/guest/home/");
		selenium.clickAt("_145_addApplication", RuntimeVariables.replace(""));

		for (int second = 0;; second++) {
			if (second >= 60) {
				fail("timeout");
			}

			try {
				if (selenium.isVisible("layout_configuration_content")) {
					break;
				}
			}
			catch (Exception e) {
			}

			Thread.sleep(1000);
		}

		selenium.saveScreenShotAndSource();
		selenium.type("layout_configuration_content",
			RuntimeVariables.replace(""));
		selenium.saveScreenShotAndSource();
		selenium.typeKeys("layout_configuration_content",
			RuntimeVariables.replace("m"));
		selenium.saveScreenShotAndSource();

		for (int second = 0;; second++) {
			if (second >= 60) {
				fail("timeout");
			}

			try {
				if (selenium.isVisible("//div[@title='Media Gallery']")) {
					break;
				}
			}
			catch (Exception e) {
			}

			Thread.sleep(1000);
		}

		selenium.saveScreenShotAndSource();
		assertTrue(selenium.isVisible("//div[@title='Media Gallery']"));
	}
}